// Write a JavaScript program to convert temperatures from celsius to fahrenheit.
// Formula:C = (5/9) * (F - 32)
// formula to convert Celsius to Fahrenheit : °F = °C × (9/5) + 32.
let celsius = 38;
let fahrenheit;
fahrenheit = celsius * (9 / 5) + 32;
console.log(`The fahrenheit temperature is:${fahrenheit}`);

let celsiusTemp = (5 / 9) * (fahrenheit - 32);
console.log(`The celsius temperature is: ${celsiusTemp}`);
